    private void btnRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistrarMouseClicked
        String cedula = this.txtCedula.getText();
        String nombreCompleto = this.txtNombreCompleto.getText();
        int fechaNacimiento = Integer.parseInt(this.txtFechaNacimiento.getText());
        String correoElectronico = this.txtCorreoElectronico.getText();
        String categoria = this.txtCategoria.getText();
        
        if (!cliente.buscarCliente(nombreCompleto, cedula)) {
        Cliente c  = new Cliente(cedula, nombreCompleto, fechaNacimiento, correoElectronico, categoria);
        cliente.apilar(c);
        JOptionPane.showMessageDialog(null,"El cliente fue registrado con exito.");
        this.dispose();
       } 
    }//GEN-LAST:event_btnRegistrarMouseClicked

