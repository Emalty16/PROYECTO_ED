    private int generarID(int numCliente) {

        int randomID = 0;
        if (numCliente >= 0) {
            randomID = (int) Math.floor(Math.random() * 10000);
            System.out.printf("El numero es....%d\n", randomID);
            generarID(numCliente - 1);
        }
        return randomID;
    }
    
