    private void btnModificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnModificarMouseClicked
        String cedula = this.txtCedula.getText();
        String nombre = this.txtNombreCompleto.getText();
        int fechaNacimiento = Integer.parseInt(this.txtFechaNacimiento.getText());
        String correo = this.txtCorreo.getText();
        String categoria = this.txtCategoria.getText();
       
        //recuperar el ID a editar
        
        String id = clienteAEditar.getCedula();

        
        //Validacion de ocupacion si se quiere cancelar un evento
        if (estado.equals("Activo")) {
            if (clientes.revisarOcupacion()) {
                JOptionPane.showMessageDialog(null, "No puedes cancelar este cliente por que esta activo", "Error", -1);
            } else {
                Cliente eventoEditado = new Cliente(id, cedula, nombre, fechaNacimiento, correo, categoria);
                clientes.editarCliente(eventoEditado);

                
                //asientos.listarHaciaAdelante();
                this.dispose();
            }
        } else {
            Cliente eventoEditado = new Cliente(id, nombre, cedula, nombre, fechaNacimiento, correo, categoria);
            clientes.apilar(eventoEditado);

            
            //asientos.listarHaciaAdelante();
            this.dispose();
        }
        
    }//GEN-LAST:event_btnModificarMouseClicked

